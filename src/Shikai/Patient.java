package Shikai;

public class Patient {

    String name;
    String Id;
    int age;
    String medicalCase;

    public Patient(){

    }

    public Patient(String name, String Id, int age, String medicalCase){
        this.name = name;
        this.Id = Id;
        this.age = age;
        this.medicalCase = medicalCase;
    }


}
